// 获取指定类别的装备
export function getEquipmentByCategory(categoryId: string): Equipment[] {
  return EQUIPMENT_DATA[categoryId as keyof typeof EQUIPMENT_DATA] || [];
}

// 根据ID获取装备
export function getEquipmentById(id: string): Equipment | undefined {
  for (const category of Object.values(EQUIPMENT_DATA)) {
    const item = category.find(eq => eq.id === id);
    if (item) return item;
  }
  return undefined;
}

// 搜索装备
export function searchEquipment(query: string, categoryId?: string): Equipment[] {
  const lowerQuery = query.toLowerCase();
  const categories = categoryId
    ? [EQUIPMENT_DATA[categoryId as keyof typeof EQUIPMENT_DATA]]
    : Object.values(EQUIPMENT_DATA);

  return categories
    .flat()
    .filter(eq => eq.name.toLowerCase().includes(lowerQuery) || eq.nameEn?.toLowerCase().includes(lowerQuery));
}
