// 装备接口定义
export interface Equipment {
  id: string;
  name: string;
  nameEn?: string; // 英文名
  price: number; // 价格（统一使用GP作为基准）
  weight: number; // 重量（磅）
  category: string; // 类别
  description?: string; // 描述
}

// 武器特殊属性
export interface Weapon extends Equipment {
  size: 'S' | 'M' | 'L'; // 尺寸
  type: 'B' | 'P' | 'S' | 'P/B' | 'P/S'; // 类型：钝击/穿刺/挥砍
  speed: number; // 速率
  damageS: string; // 对小型/中型生物伤害
  damageL: string; // 对大型生物伤害
  rateOfFire?: string; // 射速（远程武器）
  range?: { short: number; medium: number; long: number }; // 射程
  versatile?: {
    // 多功能武器（如混种剑可单手或双手使用）
    '1H': { speed: number; damageS: string; damageL: string };
    '2H': { speed: number; damageS: string; damageL: string };
  };
}

// 护甲特殊属性
export interface Armor extends Equipment {
  ac: number; // 防御等级
  armorType: 'light' | 'medium' | 'heavy' | 'shield'; // 护甲类型
}

// 货币转换率（以GP为基准）
export const CURRENCY_RATES = {
  CP: 0.01, // 1 CP = 0.01 GP
  SP: 0.1, // 1 SP = 0.1 GP
  EP: 0.5, // 1 EP = 0.5 GP
  GP: 1, // 1 GP = 1 GP
  PP: 5, // 1 PP = 5 GP
};

// 货币转换函数
export function convertToGP(amount: number, currency: keyof typeof CURRENCY_RATES): number {
  return amount * CURRENCY_RATES[currency];
}

// 格式化价格显示
export function formatPrice(gp: number): string {
  if (gp >= 1) return `${gp}gp`;
  if (gp >= 0.5) return `${gp * 2}ep`;
  if (gp >= 0.1) return `${gp * 10}sp`;
  return `${gp * 100}cp`;
}

